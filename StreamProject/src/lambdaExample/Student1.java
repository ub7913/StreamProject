package lambdaExample;

public class Student1 {
	private String name;
	private String sex;
	private int score;
	
	public Student1(String name, String sex, int score) {
		this.name=name;
		this.score=score;
		this.sex=sex;
	}

	public String getName() {
		return name;
	}

	public String getSex() {
		return sex;
	}

	public int getScore() {
		return score;
	}
	
	
}
